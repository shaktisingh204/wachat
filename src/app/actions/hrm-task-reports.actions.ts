'use server';

/**
 * HRM Task Reports — server actions.
 *
 * Auto-reports are written to `hrm_task_reports` when a worker marks a
 * roadmap task as "done" via `updateTaskStatus` in hrm-roadmaps.actions.ts.
 *
 * Tenant isolation: every read/write filters by `userId == session.user._id`.
 * Worker/manager resolution: looks up the logged-in user's employee profile
 * by email within the tenant (`crm_employees`).
 */

import { ObjectId } from 'mongodb';
import { connectToDatabase } from '@/lib/mongodb';
import { getSession } from '@/app/actions/user.actions';
import { getErrorMessage } from '@/lib/utils';
import type { CrmEmployee } from '@/lib/definitions';
import { revalidatePath } from 'next/cache';

// ─── Collection name ──────────────────────────────────────────────────────────

const COLLECTION = 'hrm_task_reports';

// ─── Types ────────────────────────────────────────────────────────────────────

export interface HrmTaskReport {
  _id: string;
  userId: string;           // tenant owner
  roadmapId: string;
  phaseId: string;
  taskId: string;           // roadmap task id
  taskTitle: string;
  workerId: string;         // employee _id who completed it
  workerName: string;       // denormalized
  assignerId: string;       // employee _id who created the task/roadmap
  assignerName: string;     // denormalized
  completedAt: string;      // ISO
  notes?: string;           // worker can add notes on completion
  acknowledgedAt?: string;  // when manager acknowledged
  acknowledgedBy?: string;  // manager employee _id
  autoGenerated: true;
}

// Shape stored in Mongo (uses ObjectId for userId/workerId/assignerId).
export interface HrmTaskReportDoc {
  _id: ObjectId;
  userId: ObjectId;
  roadmapId: string;
  phaseId: string;
  taskId: string;
  taskTitle: string;
  workerId: string;
  workerName: string;
  assignerId: string;
  assignerName: string;
  completedAt: string;
  notes?: string;
  acknowledgedAt?: string;
  acknowledgedBy?: string;
  autoGenerated: true;
  createdAt: string;
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

/** Serialises a Mongo doc to the plain-JSON HrmTaskReport shape. */
function toReport(doc: HrmTaskReportDoc): HrmTaskReport {
  return {
    _id: String(doc._id),
    userId: String(doc.userId),
    roadmapId: doc.roadmapId,
    phaseId: doc.phaseId,
    taskId: doc.taskId,
    taskTitle: doc.taskTitle,
    workerId: doc.workerId,
    workerName: doc.workerName,
    assignerId: doc.assignerId,
    assignerName: doc.assignerName,
    completedAt: doc.completedAt,
    notes: doc.notes,
    acknowledgedAt: doc.acknowledgedAt,
    acknowledgedBy: doc.acknowledgedBy,
    autoGenerated: true,
  };
}

/** Finds the calling user's employee record by session email. */
async function resolveMyEmployee(
  db: Awaited<ReturnType<typeof connectToDatabase>>['db'],
  userId: ObjectId,
  email: string,
): Promise<(CrmEmployee & { _id: ObjectId }) | null> {
  const doc = await db
    .collection<CrmEmployee>('crm_employees')
    .findOne({ userId, email });
  return doc ?? null;
}

/** Resolves a display name for a given employee ObjectId string. */
async function resolveEmployeeName(
  db: Awaited<ReturnType<typeof connectToDatabase>>['db'],
  userId: ObjectId,
  employeeId: string,
): Promise<string> {
  if (!employeeId || !ObjectId.isValid(employeeId)) return 'Unknown';
  const doc = await db
    .collection<CrmEmployee>('crm_employees')
    .findOne(
      { _id: new ObjectId(employeeId), userId },
      { projection: { firstName: 1, lastName: 1 } },
    );
  if (!doc) return 'Unknown';
  return `${doc.firstName ?? ''} ${doc.lastName ?? ''}`.trim() || 'Unknown';
}

// ─── Create (called internally by updateTaskStatus) ───────────────────────────

/**
 * Writes an auto-generated task completion report to `hrm_task_reports`.
 * Called by `updateTaskStatus` in hrm-roadmaps.actions.ts when status → "done".
 *
 * The caller supplies denormalized names so no extra Mongo round-trips are
 * needed at query time.
 */
export async function createTaskReport(
  data: Omit<HrmTaskReport, '_id' | 'userId' | 'autoGenerated'>,
): Promise<{ success: boolean; id?: string; error?: string }> {
  const session = await getSession();
  if (!session?.user) return { success: false, error: 'Not authenticated' };

  try {
    const { db } = await connectToDatabase();
    const userId = new ObjectId(String(session.user._id));
    const now = new Date().toISOString();

    const doc: Omit<HrmTaskReportDoc, '_id'> = {
      userId,
      roadmapId: data.roadmapId,
      phaseId: data.phaseId,
      taskId: data.taskId,
      taskTitle: data.taskTitle,
      workerId: data.workerId,
      workerName: data.workerName,
      assignerId: data.assignerId,
      assignerName: data.assignerName,
      completedAt: data.completedAt,
      notes: data.notes,
      autoGenerated: true,
      createdAt: now,
    };

    const result = await db.collection(COLLECTION).insertOne(doc as never);
    revalidatePath('/dashboard/hrm/portal/reports');
    return { success: true, id: result.insertedId.toString() };
  } catch (e) {
    console.error('[createTaskReport] failed:', e);
    return { success: false, error: getErrorMessage(e) };
  }
}

// ─── Manager inbox ────────────────────────────────────────────────────────────

/**
 * Returns reports where the logged-in employee is the assigner (creator/manager).
 * Optionally filtered by acknowledged state and date range.
 */
export async function getMyTaskReports(filter?: {
  acknowledged?: boolean;
  from?: string;
  to?: string;
}): Promise<HrmTaskReport[]> {
  const session = await getSession();
  if (!session?.user) return [];

  try {
    const { db } = await connectToDatabase();
    const userId = new ObjectId(String(session.user._id));

    const me = await resolveMyEmployee(db, userId, session.user.email ?? '');
    if (!me) return [];

    const query: Record<string, unknown> = {
      userId,
      assignerId: String(me._id),
    };

    if (filter?.acknowledged === true) {
      query.acknowledgedAt = { $exists: true, $ne: null };
    } else if (filter?.acknowledged === false) {
      query.$or = [
        { acknowledgedAt: { $exists: false } },
        { acknowledgedAt: null },
      ];
    }

    if (filter?.from || filter?.to) {
      const range: Record<string, string> = {};
      if (filter.from) range.$gte = filter.from;
      if (filter.to) range.$lte = filter.to;
      query.completedAt = range;
    }

    const docs = await db
      .collection<HrmTaskReportDoc>(COLLECTION)
      .find(query)
      .sort({ completedAt: -1 })
      .toArray();

    return JSON.parse(JSON.stringify(docs.map(toReport))) as HrmTaskReport[];
  } catch (e) {
    console.error('[getMyTaskReports] failed:', e);
    return [];
  }
}

