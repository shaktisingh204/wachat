// ─── Acknowledge ──────────────────────────────────────────────────────────────

/** Marks a single report as acknowledged by the calling manager. */
export async function acknowledgeReport(
  reportId: string,
): Promise<{ success: boolean; error?: string }> {
  if (!reportId || !ObjectId.isValid(reportId)) {
    return { success: false, error: 'Invalid report id.' };
  }

  const session = await getSession();
  if (!session?.user) return { success: false, error: 'Not authenticated' };

  try {
    const { db } = await connectToDatabase();
    const userId = new ObjectId(String(session.user._id));

    const me = await resolveMyEmployee(db, userId, session.user.email ?? '');
    if (!me) return { success: false, error: 'Employee profile not found.' };

    const result = await db.collection(COLLECTION).updateOne(
      {
        _id: new ObjectId(reportId),
        userId,
        assignerId: String(me._id),
      },
      {
        $set: {
          acknowledgedAt: new Date().toISOString(),
          acknowledgedBy: String(me._id),
        },
      },
    );

    if (result.matchedCount === 0) {
      return { success: false, error: 'Report not found or permission denied.' };
    }

    revalidatePath('/dashboard/hrm/portal/reports');
    return { success: true };
  } catch (e) {
    console.error('[acknowledgeReport] failed:', e);
    return { success: false, error: getErrorMessage(e) };
  }
}

/** Bulk-acknowledges multiple reports by the calling manager. */
export async function bulkAcknowledgeReports(
  ids: string[],
): Promise<{ success: boolean; count: number; error?: string }> {
  if (!ids.length) return { success: true, count: 0 };

  const validIds = ids.filter((id) => ObjectId.isValid(id));
  if (!validIds.length) return { success: false, count: 0, error: 'No valid ids.' };

  const session = await getSession();
  if (!session?.user) return { success: false, count: 0, error: 'Not authenticated' };

  try {
    const { db } = await connectToDatabase();
    const userId = new ObjectId(String(session.user._id));

    const me = await resolveMyEmployee(db, userId, session.user.email ?? '');
    if (!me) return { success: false, count: 0, error: 'Employee profile not found.' };

    const now = new Date().toISOString();
    const result = await db.collection(COLLECTION).updateMany(
      {
        _id: { $in: validIds.map((id) => new ObjectId(id)) },
        userId,
        assignerId: String(me._id),
        $or: [{ acknowledgedAt: { $exists: false } }, { acknowledgedAt: null }],
      },
      {
        $set: {
          acknowledgedAt: now,
          acknowledgedBy: String(me._id),
        },
      },
    );

    revalidatePath('/dashboard/hrm/portal/reports');
    return { success: true, count: result.modifiedCount };
  } catch (e) {
    console.error('[bulkAcknowledgeReports] failed:', e);
    return { success: false, count: 0, error: getErrorMessage(e) };
  }
}

// ─── KPIs ─────────────────────────────────────────────────────────────────────

/** Returns manager-perspective KPI cards for the Reports Inbox. */
export async function getReportKpis(): Promise<
  { label: string; value: number; hint?: string }[]
> {
  const session = await getSession();
  if (!session?.user) return [];

  try {
    const { db } = await connectToDatabase();
    const userId = new ObjectId(String(session.user._id));

    const me = await resolveMyEmployee(db, userId, session.user.email ?? '');
    if (!me) return [];

    const myId = String(me._id);

    const now = new Date();
    const startOfWeek = new Date(now);
    const day = startOfWeek.getDay(); // 0 = Sun
    startOfWeek.setDate(startOfWeek.getDate() - (day === 0 ? 6 : day - 1));
    startOfWeek.setHours(0, 0, 0, 0);
    const weekIso = startOfWeek.toISOString();

    const [total, unacknowledged, acknowledged, thisWeek] = await Promise.all([
      db.collection(COLLECTION).countDocuments({ userId, assignerId: myId }),
      db.collection(COLLECTION).countDocuments({
        userId,
        assignerId: myId,
        $or: [{ acknowledgedAt: { $exists: false } }, { acknowledgedAt: null }],
      }),
      db.collection(COLLECTION).countDocuments({
        userId,
        assignerId: myId,
        acknowledgedAt: { $exists: true, $ne: null },
      }),
      db.collection(COLLECTION).countDocuments({
        userId,
        assignerId: myId,
        completedAt: { $gte: weekIso },
      }),
    ]);

    return [
      { label: 'Total Reports', value: total },
      { label: 'Unacknowledged', value: unacknowledged, hint: 'Needs your attention' },
      { label: 'Acknowledged', value: acknowledged },
      { label: 'This Week', value: thisWeek, hint: 'Reports received this week' },
    ];
  } catch (e) {
    console.error('[getReportKpis] failed:', e);
    return [];
  }
}

// ─── Export data ──────────────────────────────────────────────────────────────

/**
 * Returns all reports in a date range for CSV export.
 * Scoped by the caller's assignerId (manager view).
 */
export async function getReportsForExport(
  from?: string,
  to?: string,
): Promise<HrmTaskReport[]> {
  const session = await getSession();
  if (!session?.user) return [];

  try {
    const { db } = await connectToDatabase();
    const userId = new ObjectId(String(session.user._id));

    const me = await resolveMyEmployee(db, userId, session.user.email ?? '');
    if (!me) return [];

    const query: Record<string, unknown> = {
      userId,
      assignerId: String(me._id),
    };

    if (from || to) {
      const range: Record<string, string> = {};
      if (from) range.$gte = from;
      if (to) range.$lte = to;
      query.completedAt = range;
    }

    const docs = await db
      .collection<HrmTaskReportDoc>(COLLECTION)
      .find(query)
      .sort({ completedAt: -1 })
      .limit(5000)
      .toArray();

    return JSON.parse(JSON.stringify(docs.map(toReport))) as HrmTaskReport[];
  } catch (e) {
    console.error('[getReportsForExport] failed:', e);
    return [];
  }
}

// ─── Utility (used by hrm-roadmaps to denormalize names) ─────────────────────

/**
 * Resolves a display name for a given employee id string within a tenant.
 * Re-exported so hrm-roadmaps.actions.ts can call it without duplicating logic.
 */
export async function resolveEmployeeNameById(
  tenantUserId: string,
  employeeId: string,
): Promise<string> {
  try {
    const { db } = await connectToDatabase();
    const userId = new ObjectId(tenantUserId);
    return resolveEmployeeName(db, userId, employeeId);
  } catch {
    return 'Unknown';
  }
}
